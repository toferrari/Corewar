#include "libft.h"

void	ft_putllnbr(long long int n)
{
	if ((-9223372036854775807 - 1) == n)
	{
		write(1, "9223372036854775808", 20);
		return ;
	}
	if (n < 0)
		n *= -1;
	if (n >= 10)
		ft_putllnbr(n / 10);
	ft_putchar(n % 10 + '0');
}
