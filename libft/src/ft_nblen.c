#include "libft.h"

int		ft_nblen(long long int nb)
{
	int	i;

	i = 0;
	if (nb < 0)
		i++;
	while (nb / 10 != 0)
	{
		i++;
		nb /= 10;
	}
	return (i + 1);
}
