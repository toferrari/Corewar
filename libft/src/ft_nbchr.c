#include "libft.h"

int	ft_nbchr(char *str, char c)
{
	int i;
	int	nb;

	i = 0;
	nb = 0;
	while (str[i])
	{
		if (str[i] == c)
			nb++;
		i++;
	}
	return (nb);
}
