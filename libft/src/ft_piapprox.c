#include "libft.h"

double	ft_piapprox(int n)
{
	double	q;
	double	pi;
	int		i;

	q = 0.0;
	pi = 2.0;
	i = 0;
	while (i < n)
	{
		q = ft_dsqrt(2 + q);
		pi = pi * (2 / q);
		i++;
	}
	return (pi);
}
